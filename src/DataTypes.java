public class DataTypes {
    public static void main(String[] args) {
        //Integer Data Type
        byte num1 = 100;
        short num2 = 1000;
        int num3 = 3902057;
        long num4 = 8295865958L;

        //floating point data type
        float num5 = 8572985.9857F;
        double num6 = 98257985.8976832d;

        //boolean data type
        boolean redLightSwitch = false;
        boolean blackLightSwitch = true;

        //Character data type
        char char1 = '%';
        char char2 = 'y';
        char char3 = '0';
        char char4 = '\n';

        //String
        String text1 = "This is a simple text";

        //Print out values

        System.out.println(num1);
        System.out.println(num6);
        System.out.println(redLightSwitch);
        System.out.println(blackLightSwitch);
        System.out.println(char4);
        System.out.println(char2);
        System.out.println(text1);

    }
}
